import Foundation

/// Render items in an ascending or descending fashion, as dictated by the `sortBy` variable.
public enum DKSortOrder : String {
    
    /// Sort in an ascending fashion
    case ascending = "asc"
    
    /// Sort in a descending fashion
    case descending = "desc"
    
}
